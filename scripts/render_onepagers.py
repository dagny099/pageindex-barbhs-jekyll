def onepager_autopsy():
    body = header("The Four-Stage Autopsy",
                  "Where retrieval failures actually live — and what to open to find them")
    # flow strip
    chip_w, chip_h, gap = 216, 46, 40
    total = 4 * chip_w + 3 * gap
    x = (W - total) / 2
    y = 168
    names = ["representation", "tree", "navigation", "synthesis"]
    for i, name in enumerate(names):
        cx = x + i * (chip_w + gap)
        body.append(f'<rect x="{cx:g}" y="{y}" width="{chip_w}" height="{chip_h}" rx="10" '
                    f'fill="{P["panel"]}" stroke="{P["primary"]}" stroke-width="2.5"/>')
        body.append(text(cx + chip_w / 2, y + 29, name, size=17, fill=P["ink"],
                         anchor="middle", weight=600))
        if i < 3:
            ax = cx + chip_w + gap / 2
            body.append(text(ax, y + 30, "→", size=22, fill=P["muted"], anchor="middle"))
    body.append(text(W / 2, y + 78,
                     "A wrong answer is four possible failures wearing one disguise. "
                     "Give each somewhere to land.",
                     size=15, fill=P["text"], anchor="middle"))

    # cards
    card_x, card_w = MARGIN, W - 2 * MARGIN
    card_h, card_gap = 226, 18
    y = 290
    col2_x = card_x + 34
    col3_x = card_x + card_w / 2 + 30
    col_chars = 52
    for name, definition, failure, check in STAGES:
        body.append(f'<rect x="{card_x}" y="{y}" width="{card_w}" height="{card_h}" rx="14" '
                    f'fill="{P["panel"]}" stroke="{P["grid"]}"/>')
        body.append(f'<path d="M{card_x},{y + 14} a14,14 0 0 1 14,-14 h-8 z" fill="none"/>')
        body.append(f'<rect x="{card_x}" y="{y + 16}" width="6" height="{card_h - 32}" rx="3" '
                    f'fill="{P["primary"]}"/>')
        body.append(text(col2_x, y + 40, name, size=20, fill=P["ink"], weight=700))
        seg, _ = para(col2_x, y + 68, definition, 100, size=14.5, fill=P["muted"])
        body += seg
        label_y = y + 104
        body.append(text(col2_x, label_y, "FAILURE SEEN IN THE STUDY", size=11.5,
                         fill=P["primary"], weight=700, style='letter-spacing="1.2"'))
        seg, _ = para(col2_x, label_y + 24, failure, col_chars, size=14, leading=20)
        body += seg
        body.append(text(col3_x, label_y, "WHAT TO OPEN AND CHECK", size=11.5,
                         fill=P["cobalt"], weight=700, style='letter-spacing="1.2"'))
        seg, _ = para(col3_x, label_y + 24, check, col_chars, size=14, leading=20)
        body += seg
        y += card_h + card_gap
    body += footer("From an 11-run measurement study of PageIndex · every number "
                   "traceable to a run artifact")
    return svg(W, H, body)


