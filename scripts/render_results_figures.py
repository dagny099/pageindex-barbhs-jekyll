#!/usr/bin/env python3
"""
render_results_figures.py — emit the three RESULTS.md figures as brand SVGs.

Derived artifacts: reports/figures/results-*.svg are generated by this script from
numbers verified against runs/ and evaluations/ (see reports/RESULTS.md §8 for the
provenance of each value). Regenerate with:

    python3 scripts/render_results_figures.py --overwrite

Stdlib only, deterministic. Palette/typography follow scripts/viz_theme.py /
reports/visual-asset-plan.md (categorical pair #C04818 + #186090 on cream,
validated with the dataviz palette validator).
"""

from __future__ import annotations

import argparse
import os
import sys

P = {
    "ground": "#F4F0E8",
    "panel": "#FBF8F0",
    "ink": "#1C3A55",
    "primary": "#C04818",   # burnt orange — the manipulated arm
    "cobalt": "#186090",    # cool counterweight — the deterministic baseline
    "text": "#2B2B2B",
    "muted": "#6B6B66",
    "grid": "#E4DDCC",
}
SANS = "'Hanken Grotesk', 'Public Sans', 'Helvetica Neue', Arial, sans-serif"
SERIF = "Fraunces, Georgia, 'Times New Roman', serif"
MONO = "'SF Mono', Menlo, Consolas, monospace"

OUT_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
                       "reports", "figures")


def esc(s: str) -> str:
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def text(x, y, s, size=13, fill=P["text"], anchor="start", weight=None,
         family=SANS, style=""):
    w = f' font-weight="{weight}"' if weight else ""
    st = f" {style}" if style else ""
    return (f'<text x="{x:g}" y="{y:g}" text-anchor="{anchor}" font-size="{size}"'
            f' font-family="{family}" fill="{fill}"{w}{st}>{esc(s)}</text>')


def title_block(fig_w, title, subtitle):
    return [
        text(40, 44, title, size=24, fill=P["ink"], weight=600, family=SERIF),
        text(40, 68, subtitle, size=13.5, fill=P["muted"]),
    ]


def rbar(x, y, w, h, fill, r=4):
    """Horizontal bar: square at the baseline (left), 4px rounded data end (right)."""
    if w <= r:
        return f'<rect x="{x:g}" y="{y:g}" width="{max(w,1):g}" height="{h:g}" fill="{fill}"/>'
    return (f'<path d="M{x:g},{y:g} h{w - r:g} a{r},{r} 0 0 1 {r},{r} v{h - 2 * r:g} '
            f'a{r},{r} 0 0 1 -{r},{r} h-{w - r:g} z" fill="{fill}"/>')


def legend(x, y, entries):
    out, cx = [], x
    for color, label in entries:
        out.append(f'<rect x="{cx:g}" y="{y - 10}" width="14" height="14" rx="3" fill="{color}"/>')
        out.append(text(cx + 20, y + 2, label, size=12.5, fill=P["text"]))
        cx += 20 + 8 * len(label) + 40
    return out


def svg(w, h, body):
    head = (f'<svg xmlns="http://www.w3.org/2000/svg" width="{w}" height="{h}" '
            f'viewBox="0 0 {w} {h}" font-family="{SANS}">\n'
            f'<rect width="{w}" height="{h}" fill="{P["ground"]}"/>')
    return head + "\n" + "\n".join(body) + "\n</svg>\n"


# --------------------------------------------------------------------------
# Figure 1 — the tree you re-send every turn
# --------------------------------------------------------------------------

def fig_tree_tax():
    W, H = 960, 560
    groups = [
        ("site-book-v1 (website, 339 nodes)", [
            ("IDX-D", "headings only", 8934, P["cobalt"]),
            ("IDX-C0", "real summaries (threshold 0)", 35516, P["primary"]),
            ("IDX-C", "default summaries (80% verbatim)", 43730, P["primary"]),
        ]),
        ("rfc9110-book-v1 (spec, 311 nodes)", [
            ("IDX-D-rfc9110", "headings only", 6793, P["cobalt"]),
            ("IDX-C0-rfc9110", "real summaries (threshold 0)", 39303, P["primary"]),
        ]),
        ("paper-book-v1 (2009 paper, 28 nodes)", [
            ("IDX-D-paper-book-v1", "headings only", 474, P["cobalt"]),
            ("IDX-PDF-vanilla-paper", "LLM-inferred tree + summaries", 4515, P["primary"]),
        ]),
    ]
    x0, x1 = 250, W - 120
    vmax = 45000
    sx = (x1 - x0) / vmax
    body = title_block(W, "The tree tax: what the agent re-sends every turn",
                       "structure tokens per tree dump — re-billed as input on every "
                       "subsequent tool call of every question")
    body += legend(x0, 96, [(P["cobalt"], "deterministic, no summaries"),
                            (P["primary"], "with generated summaries")])
    # gridlines
    gy0, gy1 = 116, H - 40
    for v in range(0, vmax + 1, 10000):
        gx = x0 + v * sx
        body.append(f'<line x1="{gx:g}" y1="{gy0}" x2="{gx:g}" y2="{gy1}" '
                    f'stroke="{P["grid"]}" stroke-width="1"/>')
        body.append(text(gx, gy1 + 18, f"{v // 1000}K", size=11.5,
                         fill=P["muted"], anchor="middle"))
    y = 140
    bar_h, bar_gap, group_gap = 24, 10, 42
    for gname, bars in groups:
        body.append(text(40, y, gname, size=13.5, fill=P["ink"], weight=600))
        y += 12
        for idx, note, v, color in bars:
            bw = v * sx
            body.append(rbar(x0, y, bw, bar_h, color))
            body.append(text(x0 - 10, y + bar_h - 7, idx, size=11.5,
                             fill=P["text"], anchor="end", family=MONO))
            label = f"{v / 1000:.1f}K" if v >= 1000 else str(v)
            full = f"{label} · {note}"
            if x0 + bw + 8 + 6.3 * len(full) > W - 12:
                # long bar: label inside the data end, on the fill
                body.append(text(x0 + bw - 10, y + bar_h - 7, full, size=11.5,
                                 fill=P["ground"], anchor="end", weight=600))
            else:
                body.append(text(x0 + bw + 8, y + bar_h - 7, full,
                                 size=11.5, fill=P["muted"]))
            y += bar_h + bar_gap
        y += group_gap
    body.append(text(40, H - 14,
                     "Single tree dump per index (structure_tokens, runs/*/run.json). "
                     "gpt-4o navigator throughout.",
                     size=11, fill=P["muted"]))
    return svg(W, H, body)


# --------------------------------------------------------------------------
# Figure 2 — website corpus: quality vs cost
# --------------------------------------------------------------------------

def fig_quality_cost():
    W, H = 760, 500
    pts = [  # (cost $/14Q, mean answer score 0-5, id, note, color)
        (0.80, 3.82, "IDX-D", "headings only", P["cobalt"]),
        (3.29, 4.11, "IDX-C", "default summaries (80% verbatim)", P["primary"]),
        (2.69, 4.54, "IDX-C0", "real summaries (threshold 0)", P["primary"]),
    ]
    x0, y0, x1, y1 = 90, 120, W - 60, H - 80   # plot rect (y0 top)
    cx = lambda c: x0 + (c / 4.0) * (x1 - x0)
    cy = lambda s: y1 - ((s - 3.4) / (5.0 - 3.4)) * (y1 - y0)
    body = title_block(W, "Website corpus: what summaries buy, and at what price",
                       "mean answer score (0–5, 14 questions, single judge) vs retrieval "
                       "cost — gpt-4o navigator, run 20260712T180445Z")
    # grid + axes labels
    for c in (0, 1, 2, 3, 4):
        gx = cx(c)
        body.append(f'<line x1="{gx:g}" y1="{y0}" x2="{gx:g}" y2="{y1}" stroke="{P["grid"]}"/>')
        body.append(text(gx, y1 + 20, f"${c}", size=11.5, fill=P["muted"], anchor="middle"))
    for s in (3.5, 4.0, 4.5, 5.0):
        gy = cy(s)
        body.append(f'<line x1="{x0}" y1="{gy:g}" x2="{x1}" y2="{gy:g}" stroke="{P["grid"]}"/>')
        body.append(text(x0 - 10, gy + 4, f"{s:.1f}", size=11.5, fill=P["muted"], anchor="end"))
    body.append(text((x0 + x1) / 2, H - 30, "cost of the 14-question run (USD)",
                     size=12.5, fill=P["ink"], anchor="middle"))
    body.append(f'<text x="26" y="{(y0 + y1) / 2:g}" text-anchor="middle" font-size="12.5" '
                f'font-family="{SANS}" fill="{P["ink"]}" '
                f'transform="rotate(-90 26 {(y0 + y1) / 2:g})">mean answer score (0–5)</text>')
    # dominance arrow C -> C0
    ax1, ay1, ax2, ay2 = cx(3.29), cy(4.11), cx(2.69), cy(4.54)
    body.append(f'<defs><marker id="arr" viewBox="0 0 10 10" refX="9" refY="5" '
                f'markerWidth="7" markerHeight="7" orient="auto-start-reverse">'
                f'<path d="M0,0 L10,5 L0,10 z" fill="{P["muted"]}"/></marker></defs>')
    body.append(f'<line x1="{ax1:g}" y1="{ay1:g}" x2="{ax2 + 14:g}" y2="{ay2 + 10:g}" '
                f'stroke="{P["muted"]}" stroke-width="1.6" stroke-dasharray="5 4" '
                f'marker-end="url(#arr)"/>')
    body.append(text(ax1 + 14, ay2 - 4, "fixing the threshold:",
                     size=11, fill=P["muted"], anchor="start"))
    body.append(text(ax1 + 14, ay2 + 12, "better AND 18% cheaper",
                     size=11, fill=P["muted"], anchor="start", weight=600))
    for c, s, idx, note, color in pts:
        px, py = cx(c), cy(s)
        body.append(f'<circle cx="{px:g}" cy="{py:g}" r="9" fill="{color}" '
                    f'stroke="{P["ground"]}" stroke-width="2"/>')
        dy = -16 if idx != "IDX-D" else -16
        body.append(text(px, py + dy, idx, size=12.5, fill=P["ink"], weight=600,
                         anchor="middle", family=MONO))
        body.append(text(px, py + 30, note, size=11, fill=P["muted"], anchor="middle"))
    body.append(text(40, H - 8,
                     "Score axis starts at 3.4 to resolve the differences; all three "
                     "conditions sit in the upper band. n=1 per cell — directional.",
                     size=10.5, fill=P["muted"]))
    return svg(W, H, body)


# --------------------------------------------------------------------------
# Figure 3 — RFC 9110 recall@fetch, two clean tests
# --------------------------------------------------------------------------

def fig_rfc_recall():
    W, H = 1015, 520
    panels = [
        ("A. Do summaries help navigation? — no",
         "same 311-node tree, summaries on vs off · run 20260715T051258Z",
         "IDX-D (headings)", "IDX-C0 (summaries)",
         [("overall (21)", 0.918, 0.687),
          ("single-hop (4)", 1.000, 0.875),
          ("multi-hop (6)", 0.881, 0.631),
          ("cross-reference (5)", 0.800, 0.550),
          ("scattered-enum (5)", 1.000, 0.680)]),
        ("B. Does representation matter? — barely",
         "same structure, Markdown lines vs PDF pages · run 20260715T174748Z",
         "IDX-D (Markdown)", "IDX-PDF-outline (PDF)",
         [("overall (21)", 0.906, 0.821),
          ("single-hop (4)", 1.000, 0.875),
          ("multi-hop (6)", 0.839, 0.756),
          ("cross-reference (5)", 0.800, 0.800),
          ("scattered-enum (5)", 1.000, 0.840)]),
    ]
    body = title_block(W, "RFC 9110: gold-section recall@fetch (objective, judge-free)",
                       "did the agent fetch the sections the answer needs — gold labels "
                       "projected onto each index's own addressing")
    pw = 430
    for pi, (ptitle, psub, base_label, var_label, rows) in enumerate(panels):
        ox = 40 + pi * (pw + 60)
        x0, x1 = ox + 150, ox + pw
        vx = lambda v: x0 + (v - 0.4) / 0.6 * (x1 - x0)
        body.append(text(ox, 112, ptitle, size=15, fill=P["ink"], weight=600))
        body.append(text(ox, 130, psub, size=11, fill=P["muted"]))
        body += legend(ox, 156, [(P["cobalt"], base_label), (P["primary"], var_label)])
        gy0, gy1 = 172, 428
        for v in (0.4, 0.6, 0.8, 1.0):
            gx = vx(v)
            body.append(f'<line x1="{gx:g}" y1="{gy0}" x2="{gx:g}" y2="{gy1}" stroke="{P["grid"]}"/>')
            body.append(text(gx, gy1 + 18, f"{v:.1f}", size=11, fill=P["muted"], anchor="middle"))
        y = 200
        for name, dv, vv in rows:
            body.append(text(x0 - 10, y + 4, name, size=12, fill=P["text"], anchor="end"))
            xd, xv = vx(dv), vx(vv)
            if abs(xd - xv) > 2:
                body.append(f'<line x1="{min(xd, xv):g}" y1="{y:g}" x2="{max(xd, xv):g}" y2="{y:g}" '
                            f'stroke="{P["grid"]}" stroke-width="2.5"/>')
                body.append(f'<circle cx="{xd:g}" cy="{y:g}" r="7" fill="{P["cobalt"]}" '
                            f'stroke="{P["ground"]}" stroke-width="2"/>')
                body.append(f'<circle cx="{xv:g}" cy="{y:g}" r="7" fill="{P["primary"]}" '
                            f'stroke="{P["ground"]}" stroke-width="2"/>')
                body.append(text(xv + (-12 if xv < xd else 12), y + 4, f"{vv:.2f}",
                                 size=10.5, fill=P["text"],
                                 anchor="end" if xv < xd else "start", weight=600))
                body.append(text(xd + (12 if xv < xd else -12), y + 4, f"{dv:.2f}",
                                 size=10.5, fill=P["text"],
                                 anchor="start" if xv < xd else "end", weight=600))
            else:  # tie — concentric with surface ring
                body.append(f'<circle cx="{xd:g}" cy="{y:g}" r="9" fill="{P["cobalt"]}"/>')
                body.append(f'<circle cx="{xd:g}" cy="{y:g}" r="5" fill="{P["primary"]}" '
                            f'stroke="{P["ground"]}" stroke-width="2"/>')
                body.append(text(xd + 14, y + 4, f"both {dv:.2f}", size=10.5,
                                 fill=P["muted"], anchor="start", weight=600))
            y += 46
        note = ("cost: $1.30 vs $6.01 (4.6×) · answer fact-scores tied (0.95)"
                if pi == 0 else
                "cost parity ($1.27 vs $1.24) · page nodes bundle neighbors")
        body.append(text(ox, 462, note, size=11.5, fill=P["text"]))
    body.append(text(40, H - 14,
                     "Recall axis starts at 0.4 (dot plot — position, not length). "
                     "24 questions; 3 absence questions have no gold recall. gpt-4o navigator.",
                     size=10.5, fill=P["muted"]))
    return svg(W, H, body)


FIGS = {
    "results-tree-tax.svg": fig_tree_tax,
    "results-website-quality-cost.svg": fig_quality_cost,
    "results-rfc-recall.svg": fig_rfc_recall,
}


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--overwrite", action="store_true",
                    help="required to replace existing figures")
    args = ap.parse_args()
    for name, fn in FIGS.items():
        path = os.path.join(OUT_DIR, name)
        if os.path.exists(path) and not args.overwrite:
            print(f"refusing to overwrite {path} (pass --overwrite)", file=sys.stderr)
            return 1
        with open(path, "w", encoding="utf-8") as f:
            f.write(fn())
        print(f"wrote {path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
