#!/usr/bin/env python3
"""
render_onepagers.py — emit the two shareable one-pagers as brand SVGs.

Derived artifacts: reports/figures/onepager-*.svg are generated by this script;
numbers come from reports/RESULTS.md (verified against runs/). Regenerate with:

    python3 scripts/render_onepagers.py --overwrite

Layout is 1080x1350 (4:5 portrait — LinkedIn feed image). Palette/typography are
shared with scripts/render_results_figures.py. Stdlib only, deterministic.
"""

from __future__ import annotations

import argparse
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from render_results_figures import MONO, OUT_DIR, P, SANS, SERIF, esc, rbar, svg, text

W, H = 1080, 1350
MARGIN = 64
SITE = "barbhs.com/blog/measuring-pageindex"


def wrap(s: str, width: int) -> list[str]:
    lines, cur = [], ""
    for word in s.split():
        cand = f"{cur} {word}".strip()
        if len(cand) <= width:
            cur = cand
        else:
            lines.append(cur)
            cur = word
    if cur:
        lines.append(cur)
    return lines


def para(x, y, s, width, size=14.5, fill=P["text"], leading=21, weight=None):
    out = []
    for i, line in enumerate(wrap(s, width)):
        out.append(text(x, y + i * leading, line, size=size, fill=fill, weight=weight))
    return out, y + len(wrap(s, width)) * leading


def header(title, subtitle):
    return [
        text(MARGIN, 96, title, size=44, fill=P["ink"], weight=600, family=SERIF),
        text(MARGIN, 132, subtitle, size=17.5, fill=P["muted"]),
    ]


def footer(line):
    y = H - 56
    return [
        f'<line x1="{MARGIN}" y1="{y - 26}" x2="{W - MARGIN}" y2="{y - 26}" stroke="{P["grid"]}" stroke-width="1.5"/>',
        text(MARGIN, y, line, size=13.5, fill=P["muted"]),
        text(W - MARGIN, y, SITE, size=13.5, fill=P["cobalt"], anchor="end", weight=600),
    ]


