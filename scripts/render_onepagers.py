def tree_glyph(x, y, s=1.0, color=None):
    """Small document-tree icon: root bar + two indented child bars + leaves."""
    c = color or P["ink"]
    rows = [(0, 26), (10, 20), (10, 20), (20, 14)]
    out = []
    for i, (indent, w) in enumerate(rows):
        out.append(f'<rect x="{x + indent * s:g}" y="{y + i * 9 * s:g}" '
                   f'width="{w * s:g}" height="{4.5 * s:g}" rx="{2 * s:g}" fill="{c}"/>')
    return out


def onepager_treetax():
    body = header("The Tree Tax",
                  "The recurring RAG cost that never shows up in the demo")

    # --- the loop diagram -------------------------------------------------
    y0 = 196
    body.append(text(MARGIN, y0, "Vectorless RAG sends the agent a tree of the document. "
                                 "The tree stays in context —", size=16, fill=P["text"]))
    body.append(text(MARGIN, y0 + 26, "so every later turn re-bills it as input.",
                     size=16, fill=P["text"], weight=600))

    turn_w, turn_h, tgap = 200, 132, 50
    total = 4 * turn_w + 3 * tgap
    tx = (W - total) / 2
    ty = y0 + 66
    labels = ["turn 1", "turn 2", "turn 3", "turn n"]
    sub = ["read the tree", "fetch a section", "fetch another", "answer"]
    for i, (lab, s) in enumerate(zip(labels, sub)):
        cx = tx + i * (turn_w + tgap)
        body.append(f'<rect x="{cx:g}" y="{ty}" width="{turn_w}" height="{turn_h}" rx="12" '
                    f'fill="{P["panel"]}" stroke="{P["grid"]}" stroke-width="1.5"/>')
        body.append(text(cx + 18, ty + 34, lab, size=17, fill=P["ink"], weight=700))
        body.append(text(cx + 18, ty + 58, s, size=14, fill=P["muted"]))
        body += tree_glyph(cx + 18, ty + 78, 1.1, P["primary"])
        body.append(text(cx + 64, ty + 100, "+ tree, again", size=13.5, fill=P["primary"],
                         weight=600))
        if i < 3:
            ax = cx + turn_w + tgap / 2
            body.append(text(ax, ty + turn_h / 2 + 7, "→", size=24, fill=P["muted"],
                             anchor="middle"))
    # formula strip
    fy = ty + turn_h + 70
    body.append(f'<rect x="{MARGIN}" y="{fy - 36}" width="{W - 2 * MARGIN}" height="56" rx="10" '
                f'fill="#F0EBDF"/>')
    body.append(text(W / 2, fy, "tree tokens  ×  turns per question  ×  questions  "
                                "=  recurring input cost", size=17, fill=P["ink"],
                     anchor="middle", weight=600, family=MONO))

    # --- measured tree sizes ----------------------------------------------
    sy = fy + 92
    body.append(text(MARGIN, sy, "How big is the tree? (same 339-node website corpus)",
                     size=19, fill=P["ink"], weight=600, family=SERIF))
    bars = [
        ("headings only", 8934, P["cobalt"], "8.9K tokens"),
        ("real summaries on every node", 35516, P["primary"], "35.5K"),
        ("“add summaries” default — 80% verbatim copies", 43730, P["primary"], "43.7K"),
    ]
    bx0, bx1 = MARGIN, W - MARGIN - 150
    sx = (bx1 - bx0) / 45000
    by = sy + 30
    for lab, v, color, vlab in bars:
        bw = v * sx
        body.append(rbar(bx0, by, bw, 30, color))
        full = f"{vlab} · {lab}"
        if bx0 + bw + 12 + 7.2 * len(full) > W - MARGIN:
            body.append(text(bx0 + bw - 14, by + 21, full, size=14, fill=P["ground"],
                             anchor="end", weight=600))
        else:
            body.append(text(bx0 + bw + 12, by + 21, full, size=14, fill=P["text"]))
        by += 46
    body.append(text(MARGIN, by + 16, "The default was the largest artifact of every "
                                      "condition built — a feature that mostly wasn't "
                                      "running, billed on every turn.",
                     size=14.5, fill=P["muted"]))

    # --- measured cost -----------------------------------------------------
    cy = by + 76
    body.append(text(MARGIN, cy, "What it cost (measured, same questions, same navigator)",
                     size=19, fill=P["ink"], weight=600, family=SERIF))
    stat_w = (W - 2 * MARGIN - 24) / 2
    for i, (corpus, low, high, mult) in enumerate([
            ("Website · 14 questions", "$0.80", "$3.29", "4.1×"),
            ("RFC 9110 · 24 questions", "$1.30", "$6.01", "4.6×")]):
        cx = MARGIN + i * (stat_w + 24)
        body.append(f'<rect x="{cx:g}" y="{cy + 22}" width="{stat_w:g}" height="140" rx="14" '
                    f'fill="{P["panel"]}" stroke="{P["grid"]}"/>')
        body.append(text(cx + 26, cy + 54, corpus, size=15, fill=P["muted"], weight=600))
        body.append(text(cx + 26, cy + 104, low, size=34, fill=P["cobalt"], weight=700))
        body.append(text(cx + 26, cy + 130, "headings tree", size=13, fill=P["muted"]))
        body.append(text(cx + stat_w / 2 + 14, cy + 104, high, size=34, fill=P["primary"],
                         weight=700))
        body.append(text(cx + stat_w / 2 + 14, cy + 130, f"summary tree · {mult}",
                         size=13, fill=P["muted"]))

    # --- closing takeaway ---------------------------------------------------
    ky = cy + 210
    body.append(f'<rect x="{MARGIN}" y="{ky}" width="{W - 2 * MARGIN}" height="118" rx="14" '
                f'fill="{P["panel"]}" stroke="{P["grid"]}"/>')
    body.append(f'<rect x="{MARGIN}" y="{ky + 16}" width="6" height="86" rx="3" '
                f'fill="{P["primary"]}"/>')
    body.append(text(MARGIN + 34, ky + 48, "Index-build cost is a one-time line item "
                                           "everyone sees. The re-sent tree is the cost "
                                           "nobody prices.",
                     size=17, fill=P["ink"], weight=600))
    body.append(text(MARGIN + 34, ky + 82, "Before enriching an index, price the "
                                           "enrichment times every turn it will ride "
                                           "along on.",
                     size=15, fill=P["text"]))

    body += footer("Structure tokens & costs from recorded runs · details in the "
                   "consolidated results")
    return svg(W, H, body)


PAGES = {
    "onepager-four-stage-autopsy.svg": onepager_autopsy,
    "onepager-tree-tax.svg": onepager_treetax,
}


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--overwrite", action="store_true",
                    help="required to replace existing files")
    args = ap.parse_args()
    for name, fn in PAGES.items():
        path = os.path.join(OUT_DIR, name)
        if os.path.exists(path) and not args.overwrite:
            print(f"refusing to overwrite {path} (pass --overwrite)", file=sys.stderr)
            return 1
        with open(path, "w", encoding="utf-8") as f:
            f.write(fn())
        print(f"wrote {path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
